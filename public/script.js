window.addEventListener('click', function() {
    var h1Tags = document.querySelectorAll('h1'); // Sélectionne toutes les balises h1

    for (var i = 0; i < h1Tags.length; i++) {
        var randomColor = getRandomColor(); // Génère une couleur aléatoire
        h1Tags[i].style.color = randomColor; // Applique la couleur aléatoire à la balise h1
    }

    function getRandomColor() {
        var letters = '0123456789ABCDEF';
        var color = '#';
        for (var i = 0; i < 6; i++) {
            color += letters[Math.floor(Math.random() * 16)];
        }
        return color;
    }
});



